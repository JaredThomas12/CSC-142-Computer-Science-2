public class MonthDays
{
    private double month;
    private double year;
    
    public void getMonth(double Mo)
    {
      month = Mo;
    }
    public void getYear(double Ye)
    {
      year = Ye;
    }
    
    public double getMonth()
    {
      return month;
    }
    public double getYear()
    {
      return year;
    }

}